var result =  '4px' - 3 // Від стрінгу, який містить цифру та букви, віднімається 3. Це не має сенсу. Значення Nan не дорівнює нічому
console.log(result); // результат: Nan.

var result = '4' - 3; // У данному випадку,  Javascript приймає стрінг за число та робить віднімання. 
console.log(result); // результат: 1

var result = '6' + 3 ** 0; // ** - це зведення у ступінь. Тут спочатку 3 возводиться у ступінь 0, а потім строка 6 приплюсовує до себе одиницю.
console.log(result); // результат: 61

var result = 12 / '6'; //Відбувається операція поділу.
// Число 12 ділеться на стрінг 6 (стрінг перетворюється у число).
console.log(result); // результат: 2

var result = '10' + (5 === 6); //Конкатенація з одночасною перевіркою на true/false. 
//оператор строгої рівності (===). 5 не дорівнюється 6.
