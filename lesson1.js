var result = 0 || '0' && 1 // Пріоритет оператора І && більше, ніж АБО ||, тому він виконується раніше.
// Отже, оператор && обчислює операнди ліворуч праворуч до першого «хибного» і повертає його, а якщо всі справжні – то останнє значення.
// A оператор || обчислює операнди ліворуч праворуч до першого «істинного» і повертає його, а якщо всі помилкові – то останнє значення.
console.log(result); // результат: 1

var result = (+null == false) < 1; // +null - це 0. (+null == false) - це true. True < 1 - це false, тому що логічна одиниця true є 1;
console.log(result); // результат: false

var result = false && true || true // Пріоритет оператора І && більше, ніж АБО ||, тому він виконується раніше. 
// Якщо лівий аргумент – false, оператор І повертає його та закінчує обчислення.
// Інакше – обчислює та повертає правий аргумент.
// false && true = false. false || true = true.
console.log(result); // результат:

var result = false && (false || true); // Тут поперше виповнюється дія у дужках. false || true = true.
// Далі, false && true = false.
console.log(result); // результат: false

var result = (+null == false) < 1 ** 5; // +null - це 0. (+null == false) - це true. 
// True < 1 у п'ятій ступені - це false, тому що логічна одиниця true є 1, а 1 у 5ій ступені =1.;
