var result = "number" + 3 + 3; //Відбувається конкатенація. До строки "number" буде додано цифри 3 та 3. 
 //Цифри не склададаються у даному випадку. 
console.log(result); // результат: number33

var result = null + 3; // null - це пусте значення, нічого не має. Тому, якщо до нічого додати 3 - буде 3.
console.log(result); // результат: 3

var result = 5 && "qwerty"; // && - Логічний оператор "АБО" (OR).Повертає true, якщо обидва операнди є true, інакше повертає false. 
// В данному випадку 5 - це не нульове число, яке відповідає true,  "qwerty" - не пуста строкк, яка також відповідає true. Отже, true && true => true. 
console.log(result); // результат: true

var result = +'40' + +'2' + "hillel"; // Відбувається конкатенація з одночасним складанням 40 та 2 тому, що перед ними стоїть унарний '+'.
console.log(result); // результат: 42hillel

var result = '10' - 5 === 6; //  Застосовується оператор строгої рівності (===). Отримуємо false тому, що ми намагаємось від стрінгу 
// відняти 5 та отримати 6. 
console.log(result); // результат: false

var result = true + false; // логічна одиниця true є 1, false = 0. Тобто, 1+0 = 1.
